			<h5>The Brief</h5>
			<p class="info"><strong>Above/</strong> Iconography for the Monitor iPhone App</p>
			
			<p>Before the launch of Apple's iOS7, Campaign Monitor had planned on launching an iPhone app to accompany their web product. The brief was to design an app that looked as native to the iPhone OS as possible, but also in-keeping with the house style.</p>

			<p>Though responsible for the concept and Art Direction of the entire app, my personal focus was on iconography &mdash; as according to official Apple documentation, a big part of the upcoming iOS was clean, single-weight icons. I believed iconography was key to making the app feel &ldquo;native&rdquo;, so my intentions were to follow Apple's guidelines, but also inject some Campaign Monitor personality where possible.</p>
			
			<div class="image under"><img class="lazyload" data-sizes="auto" data-src="../_images/monitor/sketches.png" width="100%" /></div>

			<p>As always, the iconography started with a lot of sketching (above) to nail the concept behind each glyph. Following that I began experimenting with drawing single-weight line icons (a new style for me), whilst also attempting to give the artwork a common personality.</p>
			<p>Below are some of the icons that didn't make the cut, but helped influence the final set. My main inspirations for this project were <a href="http://dribbble.com/mabu" target="_blank">Mads Burcharth</a> and <a href="http://dutchicon.com/" target="_blank">Dutch Icon</a> &mdash; both of whom combine single-weight line complexity, curves, angles and negative space to create unique-looking glyphs.</p>
