			<h5>Guessing at &ldquo;native&rdquo; style</h5>
			<p>I wanted the app to look as though it was native to the iOS7 ecosystem, and according to official Apple documentation, a big part of that was to rely on clean single-weight iconography. My intentions were to follow their guidelines, but also inject some &ldquo;Campaign Monitor&rdquo; personality into the iconography, where possible.</p>
			
			
