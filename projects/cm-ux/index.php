			<p class="info"><strong>Above:</strong> The main Canvas interface, and various individual UI elements</p>
			
			<p>Even back in the concept and architecture stages of the project, I was clear that to stand out from the competition, we needed to focus on ease-of-use. Designing emails had to fun, not complicated. To help guide my decisions, I wrote the following intentions before starting design:</p>
			
			<ol>
				<li>A beautiful and well-built email is the only acceptable outcome - regardless of content</li>
				<li>Adding and editing content should be intuitive</li>
				<li>At all points, the email should look as close to its finished state as possible. Unnecessary presentation interface controls and architecture should be avoided</li>
				<li>The interface should be preemptive of the various ways users add and interact with content, not prescriptive</li>
				<li>Content should be automatically formatted and presented in the best possible way</li>
				<li>Avoid presenting the user with errors - instead, do what we assume they were trying to achieve</li>
				<li>We should not expect a higher than necessary level of technical understanding from users</li>
				<li>The interface should operate seamlessly without knowledge of drag and drop capability</li>
			</ol>
			
