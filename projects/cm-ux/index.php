			<h5>Canvas</h5>
			<p class="info"><strong>Above:</strong> Sketching and conceptual development</p>			
			<p>One of the largest UX projects I've worked on was a product called <a href="http://canvas.cm" target="_blank">Canvas</a>; a drag and drop tool that enables customers to easily build HTML emails. Unlike the existing products in the market, I set out to design a simple interface that let the user see their finished creation (WYSIWYG) without unnecessary UI getting in the way.</p>
			
			<p>To achieve this, and to design something that we could build on over time - I set out to create a visual language, rather than jumping straight into the glossy UI. By designing guidelines like <em>"blue means draggable"</em>, I was aiming to create a product that felt tactile, playful and natural.</p>
			
